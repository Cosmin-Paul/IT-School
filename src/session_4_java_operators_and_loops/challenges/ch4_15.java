package session_4_java_operators_and_loops.challenges;

public class ch4_15 {
    public static void main(String[] args) {

        int total = 100;
        System.out.println(" The original value of total is: " + total);

        total -= 20;
        System.out.println("The value after decreasing by 20 is: " + total);

        total *= 2;
        System.out.println("The value of total after multiplyng by 2 is: " + total);

    }
}
