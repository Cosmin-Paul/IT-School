package session_4_java_operators_and_loops.challenges;

public class Challenge_14 {
    public static void main(String[] args) {

        int counter = 0;
        System.out.println("The initial value of counter number is: " + counter);

        counter++;
        System.out.println("The value incremented with 1 is: " + counter);

        counter--;
        System.out.println("The value decremented with 1 is: " + counter);

    }
}
