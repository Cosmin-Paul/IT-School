package session_4_java_operators_and_loops.challenges;

public class Challenge_15 {
    public static void main(String[] args) {

        int totalValue = 100;
        System.out.println(" The original value of total is: " + totalValue);

        totalValue -= 20;
        System.out.println("The value after decreasing by 20 is: " + totalValue);

        totalValue *= 2;
        System.out.println("The value of total after multiplyng by 2 is: " + totalValue);
    }
}