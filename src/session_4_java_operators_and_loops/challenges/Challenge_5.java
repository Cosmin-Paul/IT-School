package session_4_java_operators_and_loops.challenges;

public class Challenge_5 {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        for (int x = 1; x <= 10; x++) {
            if (x % 2 == 0) {
                System.out.println("The even numbers are: " + x);
            }
        }
    }
}