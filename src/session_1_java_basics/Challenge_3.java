package session_1_java_basics;

public class Challenge_3 {
    public static void main(String[] args) {

        int x = 10;
        int y = 2;

        int subtraction = x + y;
        int multiplication = x * y;
        int division = x / y;
        int modulus = x % y;

        System.out.println("The result of subtraction is: " + subtraction);
        System.out.println("The result of product is: " + multiplication);
        System.out.println("The result of division is: " + division);
        System.out.println("The result of modulus is: " + modulus);
    }
}
