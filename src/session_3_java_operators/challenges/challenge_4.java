package session_3_java_operators.challenges;

public class challenge_4 {
    public static void main(String[] args) {
        int count = 1;

        while (count < 10) {
            System.out.println(count);
            ++count;
        }
        while (count >= 1) {
            System.out.println(count);
            --count;
        }
    }
}