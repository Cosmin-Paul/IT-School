package session_3_java_operators.challenges;

public class ch3_4 {
    public static void main(String[] args) {
        int x = 1;

        while (x < 10) {
            System.out.println(x);
            ++x;
        }
        while (x >= 1) {
            System.out.println(x);
             --x;
        }
    }
}
