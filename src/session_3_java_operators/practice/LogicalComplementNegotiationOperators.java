package session_3_java_operators.practice;

public class LogicalComplementNegotiationOperators {
    public static void main(String[] args) {
        boolean isFirstTime = true;
        boolean isRegistred = false;
        System.out.println("Default value: isFirstTime" + isFirstTime);
        System.out.println("Updates value: isFirstTime" + isFirstTime);

        System.out.println("Default value: isRegistred" + isRegistred);
        System.out.println("updated value: isRegistred" + isRegistred);
    }
}
